package LanguagePattern;

public class MathCalculate {
    public static class Division {
        public static void main(String args[]) {
            int m, n;
            m = 99;
            n = 10;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = 15;
            n = 4;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = 9;
            n = 3;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = 0;
            n = 10;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = -1;
            n = 3;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = -10;
            n = 3;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = 10;
            n = -3;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
            m = -10;
            n = -3;
            System.out.println(m + "/" + n + "=" + m / n + "......" + m % n);
        }
    }
}
